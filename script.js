// Función para redireccionar a la página de Wikipedia del equipo
function redirectToWikipedia4(equipo) {
  // Reemplaza 'espacios' con '_' en el nombre del equipo
  equipo = equipo.replace(/ /g, "_");

  // Redirecciona a la página de Wikipedia del equipo
  window.location.href = `https://es.wikipedia.org/wiki/Sport_Boys_Association`;
}
